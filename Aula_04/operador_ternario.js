let op_ternario;

let sexo = "M";

// O operador ternário é ideal para se utilizar em pequenas expressões, para verificar se o valor da condição é true ou false, e também para verificar a condição e atribuir o valor a variável

op_ternario = sexo ? "M" : "F";

console.log(op_ternario);

op_ternario = sexo ? true : false;

console.log(op_ternario);

op_ternario = 1 ? true : false;

console.log(op_ternario);

op_ternario = NaN ? true : false;

console.log(op_ternario);


op_ternario = 0 ? true : false;

console.log(op_ternario);

